                        <p>Sejarah Dharma Wanita Persatuan berawal pada 5 Agustus 1974 saat organisasi para Isteri Pegawai
                            Republik Indonesia pada masa Pemrintahan Orde Baru itu dibentuk dengan nama Dharma Wanita.
                            Organisasi ini didirikan oleh Ketua Dewan Pembina KORPRI saat itu, Amir Machmud, atas prakarsa
                            Ibu Tien Soeharto sebagai Ibu Negara, pada waktu itu Dharma Wanita beranggotakan para Istri
                            Pegawai Republik Indonesia, Anggota ABRI yang dikaryakan dan Pegawai BUMN. Pada era Reformasi
                            tahun 1998, organisasi wanita ini melakukan perubahan mendasar, tidak ada lagi muatan politik
                            dari Pemerintah. Dharma Wanita menjadi organisasi sosial kemasyarakatan yang netral dari
                            politik, Independen dan Demokrasi Nama Dharma Wanita kemudian berubah menjadi Dharma Wanita
                            Persatuan, penambahan kata “Persatuan” disesuaikan dengan nama Kabinet Persatuan Nasional
                            dibawah kepemimpinan Presiden Abdurrahman Wahid. Perubahan organisasi ini tidak terbatas pada
                            penambahan kata Persatuan namun juga berubah menjadi organisasi yang mandiri dan Demokrasi. Pada
                            Munas Luar Biasa (Munaslub) Dharma Wanita yang diselenggarakan pada tanggal 6 – 7 Desember 1999,
                            seluruh rancangan Anggaran Dasar disahkan dan menetapkan Ketua Umum Dharma Wanita Persatuan
                            terpilih, Ny. Dr. Nila F. Moeloek. Pokok-pokok perubahan organisasi Dharma Wanita yang
                            ditetapkan pada Munaslub antara lain :</p>
                        <ul>
                            <li>1. Nama organisasi berubah menjadi Dharma Wanita Persatuan;</li>
                            <li>2. Istilah Istri Pegawai Republik Indonesia diganti menjadi Istri Pegawai Negeri Sipil
                                Republik Indonesia;</li>
                            <li>3. Penegasan sebagai organisasi sosial kemasyarakatan yang bergerak dibidang Pendidikan,
                                Ekonomi dan Sosial Budaya</li>
                            <li>4. Penegasan sebagai organisasi non politik;</li>
                            <li>5. Penerapan demokrasi dalam organisasi dalam organisasi (Ketua Umum dan Ketua pada Unsur
                                Pelaksana dipilih secara Demokrasi).</li>
                        </ul>
                        <p>Sebagai salah satu organisasi masyarakat (ormas) perempuan terbesar di Indonesia, sudah
                            selayaknya Dharma Wanita Persatuan memiliki Standing Position dan mengambil peran strategis
                            dalam konstalasi pembangunan nasional, sebagaimana ormas lainnya, Dharma Wanita Persatuan
                            memiliki peluang untuk berkiprah lebih luas dengan mengoptimalisasikan peran sertanya
                            sebagaimana yang diatur pada pasal 5 Undang-undang Nomor 17 Tahun 2013 Tentang Organisasi
                            Kemasyrakatan, (“UU No.17 Th. 2013”) yang menyebutkan bahwa pembentukan ormas bertujuan :</p>
                        <ul>
                            <li>1. Meningkatkan partisipasi dan keberdayaan masyarakat;</li>
                            <li>2. Memberikan pelayanan kepada masyarakat;</li>
                            <li>3. Menjaga nilai agama dan kepercayaan terhadap Tuhan Yang Maha Esa;</li>
                            <li>4. Melestarikan dan memelihara norma, nilai, moral, etika dan budaya yang hidup dalam
                                masyarakat;</li>
                            <li>5. Melestarikan sumber daya alam dan lingkungan hidup;</li>
                            <li>6. Mengembangkan kesetiakawanan sosial, gotong royong dan bertoleransi dalam kehidupan
                                bermasyarakat;</li>
                            <li>7. Menjaga, memelihara dan memperkuat persatuan dan kesatuan Bangsa, dan</li>
                            <li>7. Mewujudkan tujuan</li>
                        </ul>
                        <p>Dengan mengacu pada ketentuan pasal 37 ayat 1 UU No. 17 Th.2013 , yang menyatakan bahwa keuangan
                            ormas dapat bersumber dari :</p>
                        <ul>
                            <li>1. Iuran Anggota;</li>
                            <li>2. Bantuan/ sumbangan masyarakat;</li>
                            <li>3. Hasil usaha Ormas;</li>
                            <li>4. Bantuan/sumbangan dari orang asing/ lembaga asing;</li>
                            <li>5. Kegiatan lain yang sah menurut hukum, dan /</li>
                            <li>6. Anggaran pendapatan belanja negara dan / anggaran pendapatan belanja</li>
                        </ul>
                        <p>Maka sumber keuangan Dharma Wanita Persatuan tidak akan bertentangan dengan yang telah diatur
                            dalam UU No. 17 Th. 2013.</p>
                        <p>Pada sisi lain, dengan telah diterbitkannya Undang-undang Nomor 5 tahun 2014 tentang Aparatur
                            Sipil Negara, maka Dharma Wanita Persatuan harus meyelaraskan diri dengan tuntutan perubahan
                            lingkungan sekitarnya. Pemerintah telah membuat Rancangan Teknokratis Rencana Pembangunan Jangka
                            Menengah Nasional (RPJMN) yang dilengkapi dengan Visi Misi kebijakan dan program Presiden
                            terpilih dan ditetapkan menjadi RPJMN, sehingga Dharma Wanita Persatuan selalu berusaha
                            menyesuaikan dan menetapkan Rencana Strategis Organisasi yang sejalan dangan arahan dan agenda
                            RPJMN yang ditetapkan oleh Pemerintah.</p>
                        <p>Hal ini dilakukan dalam rangka meningkatkan kapabilitas dan kapasitas Dharma Wanita Persatuan
                            untuk pencapaian Pembangunan Nasional sebagaiamana di tuangkan dalam RPJMN tersebut. Pada
                            Musyawarah Nasional (“Munas”) Ke IV Dharma Wanita Persatuan yang dilaksanakan pada tanggal 12 –
                            13 Desember 2019 telah menghasilkan beberapa keputusan penting yaitu antara lain Perubahan
                            Anggaran Dasar Dharma Wanita dan Rencana Strategis Dharma Wanita Persatuan untuk tahun 2020 –
                            2024.</p>
                        <p>Sesuai hasil Munas Ke IV, perubahan mendasar dalam Anggaran Dasar Dharma Wanita Persatuan yaitu
                            antara lain tentang ketentuan atau pasal-pasal mengenai :</p>
                        <ul>
                            <li>1. Ketua Umum</li>
                            <p>Dijabat oleh isteri Menteri yang membidangi Aparatur Negara.</p>
                            <li>2. Ketua DWP</li>
                            <p>Jabatan Ketua DWP melekat pada isteri Sekjen/ Sesmenko/ Sesmen/ Sestama/ Sekda serta isteri
                                kepala LPNK</p>
                            <li>3. Pengurus DWP</li>
                            <p>Pengurus DWP dijabat oleh isteri Aparatur Sipil Negara aktif</p>
                            <li>4. Ketua DWP/ Provinsi/ Kabupaten/ Kota/ Kecamatan/ Kelurahan</li>
                            <p>Sebelumnya dipilih menjadi ex – oficio. Masa jabatan mengikuti masa jabatan suami.</p>
                            <li>5. Dewan Kehormatan DWP</li>
                            <p>Beranggotaka isteri mantan Presiden atau Wakil Presiden dan mantan Ketua Umum</p>
                            <li>6. Dewan Penasihat</li>
                            <p>Penambahan Ketua MK, Ketua KY, dan pejabat setingkat Menteri</p>
                        </ul>
